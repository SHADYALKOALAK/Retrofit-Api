package com.shadyalkolak.retrofit.models;

public class GeoModel {
    private String lat;
    private String lng;

    // Getter & Setter methods
    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
